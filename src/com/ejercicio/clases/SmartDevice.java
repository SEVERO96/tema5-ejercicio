package com.ejercicio.clases;

public class SmartDevice {

    public String marca;
    public String modelo;
    public int precio;
    public int year;

    public SmartDevice(){

    }

    public SmartDevice(String marca, String modelo, int precio, int year) {
        this.marca = marca;
        this.modelo = modelo;
        this.precio = precio;
        this.year = year;
    }
}
